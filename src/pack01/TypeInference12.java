package pack01;

public class TypeInference12 {

	public static void main(String[] args) {
		// var(참조형 변수선언) : 데이터 형태를 보고 알아서 판단해 타입 지정
		// javascript 에서 많이 사용함, java에서도 사용가능
		
		var i = 10; // int
		var d = 10.0; // double
		var str = "Temp"; // String
		
		System.out.println(i);
		System.out.println(d);
		System.out.println(str);

	}

}
