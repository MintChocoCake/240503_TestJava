package pack01;

public class DoubleEx07 {

	public static void main(String[] args) {
		
		// 정수(int) 와 실수(float) 연산 시 더 정밀한 수로 자동 형 변환
		int aVal = 10; // 4byte
		float bVal = 10.5f; // 4byte
		
		System.out.println("aVal + bVal : " + (aVal + bVal));
		
		// 평균을 낼 땐 double 자료형을 쓰는 것이 좋다
		
		
	}

}
