package pack02;

public class _05_Relatest {

	public static void main(String[] args) {
		
		// A : 65 , B : 66
		char a = 'A', b = 'B';
		
		boolean AL = true;
		
		System.out.println(AL);
		
		// a가 크다
		System.out.println("a>b : " + (a>b));
		
		// b가 크다
		System.out.println("a<b : " + (a<b));
		
		// a와 b가 같지 않다
		System.out.println("a!=b : " + (a!=b));
		
		int num1 = 5, num2 = 3;
		
		// num1 이 크다
		System.out.println("num1>num2 : " + (num1>num2));
		
		// num1이 크거나 같다
		System.out.println("num1>=num2 : " + (num1>=num2));
		
		// num2가 크거나 같다
		System.out.println("num1<=num2 : " + (num1<=num2));
		
		// num1 과 num2가 같다
		System.out.println("num1==num2 : " + (num1==num2));
		
		// num1 과 num2가 같지 않다
		System.out.println("num1!=num2 : " + (num1!=num2));
		
	}

}
